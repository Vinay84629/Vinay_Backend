public class CaseConversion {
    public static void main(String[] args) {
        //Convert the string String str = "Java is FUN!"; to lowercase and uppercase.

        String abc = "Java is FUN!";

        String case1 = abc.toLowerCase();
        System.out.println(case1);

        String case2 = abc.toUpperCase();
        System.out.println(case2);
    }
}
//The toLowerCase() and toUpperCase() methods convert all characters in the string to lowercase and uppercase, respectively.